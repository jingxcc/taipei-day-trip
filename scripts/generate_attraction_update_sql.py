import json
from pathlib import Path


BASE_DIR = Path(__file__).resolve().parent.parent
API_DATA_PATH = BASE_DIR / "data" / "taipei-api-attractions.json"
COMPARE_REPORT_PATH = BASE_DIR / "data" / "attraction-compare-report.json"
MAPPING_PATH = BASE_DIR / "data" / "attraction-manual-mapping.json"
OUTPUT_SQL_PATH = BASE_DIR / "database" / "update_attractions.sql"


# Category is not synchronized.
# Existing category assignments are preserved.
UPDATE_RULES = {
    "matched": {
        "description": False,
        "images": True,
    },
    "rename_and_update": {
        "name": True,
        "description": True,
        "images": True,
    },
}


def load_json(path):
    with path.open("r", encoding="utf-8") as file:
        return json.load(file)


def escape_sql(value):
    if value is None:
        return "NULL"

    escaped = str(value).replace("'", "''")
    return f"'{escaped}'"


def build_api_by_name(api_attractions):
    api_by_name = {}

    for item in api_attractions:
        api_by_name[item["name"].strip()] = item

    return api_by_name

def generate_attraction_update_sql(old_name, new_name, api_attraction, update_rule):
    sql_lines = []
    update_parts = []

    if update_rule.get("name"):
        update_parts.append(f"attraction_name = {escape_sql(new_name)}")

    if update_rule.get("description"):
        update_parts.append(
           f"description = {escape_sql(api_attraction.get('introduction').strip())}"
        )

    update_parts.append("is_active = TRUE")

    if update_parts:
        sql_lines.append(
            "UPDATE attraction\n"
            f"SET {', '.join(update_parts)}\n"
            f"WHERE attraction_name = {escape_sql(old_name)};"
        )

    return sql_lines

