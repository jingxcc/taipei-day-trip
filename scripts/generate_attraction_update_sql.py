def generate_image_update_sql(attraction_name, api_attraction):
    sql_lines = []
    images = api_attraction.get("images", [])

    sql_lines.append(
        "DELETE iu FROM image_url iu\n"
        "JOIN attraction a ON iu.attraction_id = a.id\n"
        f"WHERE a.attraction_name = {escape_sql(attraction_name)};"
    )

    for image in images:
        image_url = image["src"]

        sql_lines.append(
            "INSERT INTO image_url (attraction_id, url)\n"
            f"SELECT id, {escape_sql(image_url)}\n"
            "FROM attraction\n"
            f"WHERE attraction_name = {escape_sql(attraction_name)};"
        )

    return sql_lines

def generate_deactivate_sql(old_name):
    return [
        "UPDATE attraction\n"
        "SET is_active = FALSE\n"
        f"WHERE attraction_name = {escape_sql(old_name)};"
    ]


def main():
    api_attractions = load_json(API_DATA_PATH)
    compare_report = load_json(COMPARE_REPORT_PATH)
    mapping = load_json(MAPPING_PATH)

    api_by_name = build_api_by_name(api_attractions)

    sql_lines = [
        "-- Generated by scripts/generate_attraction_update_sql.py",
        "-- Review before running on dev or EC2 database.",
        "",
    ]

    sql_lines.extend(["-- matched", ""])
    matched_names = compare_report.get("matched_names", [])

    for name in matched_names:
        api_attraction = api_by_name.get(name)

        if not api_attraction:
            print(f"Warning: API attraction not found: {name}")
            continue

        # if UPDATE_RULES["matched"]["description"]:
        #     sql_lines.extend(
        #         generate_attraction_update_sql(
        #             old_name=name,
        #             new_name=name,
        #             api_attraction=api_attraction,
        #             update_rule=UPDATE_RULES["matched"],
        #         )
        #     )

        if UPDATE_RULES["matched"]["images"]:
            sql_lines.extend(generate_image_update_sql(name, api_attraction))

        sql_lines.append("")
    
    sql_lines.extend(["-- rename_and_update", ""])
    rename_and_update = mapping.get("rename_and_update", {})
    for old_name, new_name in rename_and_update.items():
        api_attraction = api_by_name.get(new_name)

        if not api_attraction:
            print(f"Warning: API attraction not found: {new_name}")
            continue
        
        if UPDATE_RULES["rename_and_update"]["name"] or UPDATE_RULES["rename_and_update"]["description"]:
            sql_lines.extend(
                generate_attraction_update_sql(
                    old_name=old_name,
                    new_name=new_name,
                    api_attraction=api_attraction,
                    update_rule=UPDATE_RULES["rename_and_update"],
                )
            )

        if UPDATE_RULES["rename_and_update"]["images"]:
            sql_lines.extend(generate_image_update_sql(new_name, api_attraction))

        # sql_lines.extend(generate_rename_update_sql(old_name, new_name, api_attraction))
        sql_lines.append("")

    sql_lines.extend(["-- replaced_by_other_attraction", ""])
    replaced = mapping.get("replaced_by_other_attraction", {})
    for old_name in replaced.keys():
        sql_lines.extend(generate_deactivate_sql(old_name))
        sql_lines.append("")

    sql_lines.extend(["-- removed", ""])
    removed = mapping.get("removed", [])
    for old_name in removed:
        sql_lines.extend(generate_deactivate_sql(old_name))
        sql_lines.append("")

    # Only deactivate old attractions here.
    # Split attraction INSERT SQL is generated separately.
    sql_lines.extend(["-- split", ""])
    split = mapping.get("split", {})
    for old_name in split.keys():
        sql_lines.extend(generate_deactivate_sql(old_name))
        sql_lines.append("")

    OUTPUT_SQL_PATH.parent.mkdir(parents=True, exist_ok=True)

    with OUTPUT_SQL_PATH.open("w", encoding="utf-8") as file:
        file.write("\n".join(sql_lines))

    print(f"SQL generated: {OUTPUT_SQL_PATH}")


if __name__ == "__main__":
    main()